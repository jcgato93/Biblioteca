private void apply_filters(){
    {
    int x=0;
    String query="";
    if(!txtGetNombre1.getText().equals(""))
    {
        if(x==0)
        {
         query="WHERE ";
         x++;
        }
        else
        {
         query="AND ";
        }
        query=query+"cli.Nombre like '"+txtGetNombre1.getText()+"%' ";
    }
    
    if(!txtGetApellido1.getText().equals(""))
    {
      if(x==0)
      {
       query="WHERE ";
       x++;
      }
      else
      {
          query="AND ";
      }
      query=query+"cli.Apellido like '"+txtGetApellido1.getText()+"%' ";
    }
        llenar_Tabla(query);
    }
}
  
    
  private  void llenar_Tabla(String filtro) {
        try {
            DefaultTableModel modelo;
            NUsuarios Usuario = new NUsuarios();
            modelo= Usuario.fillDataTable(filtro);
            
            jTableUsuario.setModel(modelo);

        } catch (Exception e) {
            JOptionPane.showConfirmDialog(rootPane, e);
        }
  }
