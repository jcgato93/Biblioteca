    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:

        if(!txtUsuNombre.getText().equals("") && !txtUsuApellido.getText().equals("") && !txtUsuario.getText().equals("") && !txtUsuContraseña.getText().equals(""))
        {
            DUsuarios obj=new DUsuarios();

            obj.setUserId(Integer.parseInt(txtUserId.getText()));
            obj.setNombre(txtUsuNombre.getText());
            obj.setApellido(txtUsuApellido.getText());
            obj.setLogin(txtUsuario.getText());
            obj.setPassword(txtUsuContraseña.getText());

            boolean result= libroTools.actualizar(obj);

            if(result)
            {
                JOptionPane.showMessageDialog(null,"Se actualizo correctamente");
            }
            else
            {
                JOptionPane.showMessageDialog(null,"No se pudo Actualizar");
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null,"Faltan Datos");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

