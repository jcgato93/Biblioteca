    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        DUsuarios obj=new DUsuarios();

        obj.setUserId(Integer.parseInt(txtUserId.getText()));
        obj.setNombre(txtUsuNombre.getText());
        obj.setApellido(txtUsuApellido.getText());
        obj.setLogin(txtUsuario.getText());
        obj.setPassword(txtUsuContraseña.getText());

        boolean isExiste=libroTools.VerificaUser(obj);

        if(isExiste)
        {
            JOptionPane.showMessageDialog(null,"Ya Existe el Usuario en la Base de Datos");
        }
        else
        {
            boolean result= libroTools.insertar(obj);

            if(result)
            {
                JOptionPane.showMessageDialog(null, "Se inserto el Registro Correctamente");
            }
            else
            {
                JOptionPane.showMessageDialog(null, "No se logro ingresar el Registro");
            }

        }

    }//GEN-LAST:event_btnGuardarActionPerformed

